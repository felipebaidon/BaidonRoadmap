STyp Sprites_PlayerShip;
STyp Enemy[NUMBER_OF_ENEMIES];
STyp Missile; 
	
/* File Scope Functions */
static void Sprites_MoveEnemies(void);
static void Sprites_MoveMissile(void);
static void Sprites_AdvanceEnemies(unsigned int *invadersDirection);
	
void Sprites_Init(unsigned long shipPosition){ int i;
  for(i=0;i<NUMBER_OF_ENEMIES;i++){
    Enemy[i].x = 15*i;
    Enemy[i].y = 10;
    Enemy[i].image = SmallEnemy30PointA;
    Enemy[i].life = 1;
   }
	
	 Sprites_PlayerShip.x = shipPosition;
	 Sprites_PlayerShip.y = HEIGHT_OF_DISPLAY;
	 Sprites_PlayerShip.image = PlayerShip0;
	 Sprites_PlayerShip.life = 1;
}
void Sprites_Move(void)
{
	Sprites_MoveEnemies();
	Sprites_MoveMissile();
}

static void Sprites_MoveEnemies(void){ 
	int i;
	static unsigned int invadersDirection  = 0; //0 for right 1 for left 
	
	if(!invadersDirection )
	{
		for(i = 0; i < NUMBER_OF_ENEMIES; i++)
		{
			if(Enemy[i].life)
			{
				Enemy[i].x += 2;
			}	
		}
		
		if(Enemy[NUMBER_OF_ENEMIES - 1].x >= 65)
		{
			Sprites_AdvanceEnemies(&invadersDirection);
		}
	}
	else
	{
		for(i = 0; i < NUMBER_OF_ENEMIES; i++)
		{
			if(Enemy[i].life)
			{
				Enemy[i].x -= 2;
			}	
		}
		
		if(Enemy[0].x  < 1)
		{		
			Sprites_AdvanceEnemies(&invadersDirection);
		}
	}
}

static void Sprites_AdvanceEnemies(unsigned int *invadersDirection)
{
	unsigned int i;
	
	for(i = 0; i < NUMBER_OF_ENEMIES; i++)
	{
		Enemy[i].y += 10;
	}

	*invadersDirection ^= 1;
}

static void Sprites_MoveMissile(void)
{
	if(Missile.y > 0 && Missile.life)
	{
		Missile.y -= 4;
	}
	else
	{
		Missile.life = 0;
	}
	
}

void Sprites_MovePlayerShip(unsigned long position)
{
	Sprites_PlayerShip.x = position;
}

void Sprites_Draw(void){ int i;
  Nokia5110_ClearBuffer();
  for(i=0;i<NUMBER_OF_ENEMIES;i++){
    if(Enemy[i].life > 0){
     Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image, 0);
    }
  }
	Nokia5110_PrintBMP(Sprites_PlayerShip.x, Sprites_PlayerShip.y, Sprites_PlayerShip.image, 0);
	
	if(Missile.life > 0)
	{
			Nokia5110_PrintBMP(Missile.x, Missile.y, Missile.image, 0);
	}
	
  Nokia5110_DisplayBuffer();      // draw buffer
}

void Sprites_SendMissile(unsigned long position)
{
	if(!Missile.life)
	{
		Missile.x = position;
		Missile.y = HEIGHT_OF_DISPLAY - PLAYER_SHIP_HEIGHT;
		Missile.image = Missile1;
		Missile.life = 1;
	}
}
